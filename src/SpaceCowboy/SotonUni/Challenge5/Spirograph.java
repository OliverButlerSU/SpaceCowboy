package SpaceCowboy.SotonUni.Challenge5;

public class Spirograph {

    private final double R;
    private final double r;
    private final double a;

    public Spirograph(double R, double r, double a){
        this.R = R;
        this.r = r;
        this.a = a;
    }

    public double getR() {
        return R;
    }

    public double getr(){
        return r;
    }

    public double getA() {
        return a;
    }
}
